#include <stdio.h>

#define MAX 1000000
int phi[MAX + 1];      // ??????????????¢??°??????
char isprime[MAX + 1]; // ?´???°????????¨
long long fn[MAX + 1]; // ??¬?????????Farey Sequence

void euler_phi() {
  int i, j;

  for (i = 0; i <= MAX; i++) {
    phi[i] = i;
    //		isprime[i] = 0;
  }
  phi[2] = 1;
  for (j = 4; j <= MAX; j += 2)
    isprime[j] = 1, phi[j] -= phi[j] >> 1;
  for (i = 3; i <= MAX; i++) {
    if (isprime[i])
      continue;
    phi[i] -= phi[i] / i;
    for (j = i * i; j <= MAX; j += i)
      isprime[j] = 1, phi[j] -= phi[j] / i;
  }
}

char buf[10], *p;
int getint() {
  int n = 0;
  while (*p >= '0')
    n = (n << 3) + (n << 1) + (*p++ & 0xf);
  return n;
}
int main() {
  int t, n;

  euler_phi();
  fn[0] = 1;
  for (n = 1; n <= MAX; n++)
    fn[n] = fn[n - 1] + phi[n];

  fgets(p = buf, 10, stdin), t = getint();
  while (t-- > 0) {
    fgets(p = buf, 10, stdin);
    printf("%lld\n", fn[getint()]);
  }
  return 0;
}