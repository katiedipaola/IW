int lehvenstein_distance(int n, int m, char *s, char *t) {
  int **dp;
  int i, j;
  int ans;

  dp = (int **)malloc(sizeof(int *) * (n + 1));

  for (i = 0; i <= n; i++) {
    dp[i] = (int *)malloc(sizeof(int) * (m + 1));
    for (j = 0; j <= m; j++)
      dp[i][j] = 1001001001;
  }

  for (i = 0; i <= n; i++)
    dp[i][0] = i;
  for (j = 0; j <= m; j++)
    dp[0][j] = j;

  for (i = 1; i <= n; i++) {
    for (j = 1; j <= m; j++) {
      dp[i][j] = min(dp[i][j], dp[i - 1][j] + 1);
      dp[i][j] = min(dp[i][j], dp[i][j - 1] + 1);
      dp[i][j] = min(dp[i][j], dp[i - 1][j - 1] + (s[i - 1] != t[j - 1]));
    }
  }

  ans = dp[n][m];

  for (i = 0; i <= n; i++)
    free(dp[i]);
  free(dp);

  return (ans);
}

